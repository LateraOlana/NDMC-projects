          <h1 class="h3 mb-4 text-gray-800">Current Policy Options/Interventions to respond to COVID-19 Pandemic</h1>
          <hr>
          <h2 class="h3 mb-4 text-gray-800">Executive Summary</h2>
          <span >On 5th April, 2020, the Novel Coronavirus 2019 (COVID-19) has already affected 208 countries and territories
around the world and 2 international conveyances since December 2019. It has already infected 1,225,047
people globally and 43 persons in Ethiopia since December 2019, and killed 66, 503 people globally and 2
persons in Ethiopia during the same period. Currently, COVID-19 has no specific antiviral agent for the
treatment of the infection and there is no vaccine to prevent. As a result, implementation of public health
prevention and control measures that aim slowing down and stopping of transmission is crucial. Detecting and
testing suspected cases and tracking contacts with suspected and/or confirmed cases, individual social
distancing that focuses on isolation of case, quarantine of contacts, and stay-at home; social distancing
affecting multiple persons such as closure of educational institutions and workplaces, measures for special
populations, mass gathering cancellations, and mandatory quarantine of a building or residential area; and
environmental disinfection and use of personal protective equipment are currently available policy options to
fight the disease. Considerations of local context in response to COVID-19 measures are crucial for slowing
down and controlling the pandemic in order to minimize the negative social and economic consequences of the
prevention and control measures.
          </span>
          <hr>
          <h2 class="h3 mb-4 text-gray-800">Key Findings</h2>

            <ul>
              <li><span>Implementation of public health control
measures that aim prevention of transmission is
crucial to combat the COVID-19 pandemic</span></li>
              <li><span>A combination of interventions, especially non-
pharmaceutical interventions, should be in place to contain the spread of the COVID-19 which
may be most effective</span></li>
              <li><span>Considerations of local context in response to
COVID-19 measures are crucial for slowing
down and controlling the pandemic</span></li>

            </ul>
          <hr>
          <h2 class="h3 mb-4 text-gray-800">Background</h2>
           <span>
           By April 05, 2020, according to World Population,
the coronavirus COVID-19 is affecting 208
countries and territories around the world and 2
international conveyances. The disease has already
infected 1,225,047 people globally since December
2019. In Ethiopia, the virus has already infected 43
individuals from March 12 to April 05, 2020.       </span><br>
          <img  alt="img" src="img/wm.png" style="max-height: 100%; max-width: 100%"><br>
          <span><b>Global cases:</b> Source: Worldometer, COVID-19.
Last updated: April 05, 2020, 14:14 GMT</span>
          <br><br>
          <span>In the same period the disease has already claimed
the life of 66,503 people and 2 persons in Ethiopia.</span><br>
          <img  alt="img" src="img/wm2.png" style="max-height: 100%; max-width: 100%"><br>
          <span><b>Global deaths:</b> Source: Worldometer, COVID-19
Pandemic. Last updated: April 05, 2020,14:14 GMT</span>
          <br><br>
          <span>Currently, COVID-19 has no specific antiviral
agent for the treatment of the infection and there is
no vaccine to prevent. As a result, implementation
of public health control measures that aims
prevention of transmission is crucial.</span>
          <hr>
          <h2 class="h3 mb-4 text-gray-800">Policy Options</h2>

<span>
          Pharmaceutical solutions, including vaccines and
treatments for COVID-19, have been reported to
take 12-18 months to develop. The immediate
solution for slowing down the COVID-19 pandemic
and minimize the social and economic catastrophe
of the disease is to focus on prevention and control

mechanisms. Evidence show shat a variety of non-
pharmaceutical interventions have been

implemented to curve the diseases. The policy
options that have been implemented include:
detection and testing, social distancing affecting individual and multiple persons, environmental
disinfection and use of personal protective
equipment.         </span>

           <hr>
<h2 class="h3 mb-4 text-gray-800">1. Detecting and Testing</h2>
           <span>
           Identification suspected
cases and follow-up of contacts is crucial. Testing
could be done for both suspected cases or the whole
However, in a situation like this, the better option is
to test the suspect cases. Testing everybody will not
be feasible for countries with limited resources and
may give a false sense of security if the test result is
negative.        </span>

<hr>
          <h2 class="h3 mb-4 text-gray-800">2. Individual Social Distancing</h2>
          <h3 class="h3 mb-4 text-gray-800">2.1 Isolation of cases:</h3>
           <span>
           This is for confirmed or
suspected cases of COVID19. They are either
hospitalized (usually for moderate or severe cases)
to provide care, or managed in dedicated isolation
facilities or at home (mild cases).</span><br><br>
          <h3 class="h3 mb-4 text-gray-800">2.2 Quarantine of contacts:</h3>
          <span>
         This is for healthy
person(s) who have had a high- or low-risk contact
with a confirmed COVID-19 case. Quarantine of
cases can be voluntary or mandatory. Resistance
and non-compliance will be greater if impacts of
this policy are inequitable, and for those on low
incomes, loss of income means inability to pay for
necessities.       </span><br><br>

          <h3 class="h3 mb-4 text-gray-800">2.3 Stay-at-home:</h3>
          <span>
         This a comprehensive
recommendation for the public to stay at home and
avoid mass gatherings and close contacts with
people, especially known high-risk groups.
Concerns are likely to arise about impact on others
within the household.       </span>

<hr>
          <h2 class="h3 mb-4 text-gray-800">3. Social Distancing affecting multiple persons</h2>
          <h3 class="h3 mb-4 text-gray-800">3.1 Closure of educational institutions:</h3>
           <span>
           It refers to
closure of schools (including day care centres,
kindergartens, primary and secondary schools), and
higher educational institutions (including
universities, research institutes, etc.).</span><br><br>
          <h3 class="h3 mb-4 text-gray-800">3.2 Workplace closure:</h3>
          <span>
        Closure of offices, factories,
retail outlets, agricultural production, construction,
restaurants, cafes/bars, sports clubs,haulage/transport etc. It can be flexible working
schedules/shifts for employees; opportunities for
distance working/teleworking; encouraging physical
distancing measures within the workspace;
increased use of email and teleconferences to
reduce close contacts; reduced contact between
employees and customers; reduced contact between
employees; adoption of flexible leave policies; and
promoting the use of other personal protective and
environmental countermeasures.       </span><br><br>

          <h3 class="h3 mb-4 text-gray-800">3.3 Measure for special populations:</h3>
          <span>
         It refers to
measures to limit outside visitors and limit the
contact between the inmates/patients in confined
settings, such as long-term care facilities, either for
the elderly or persons with special needs;
psychiatric institutions; homeless shelters; and
prisons.       </span><br><br>

            <h3 class="h3 mb-4 text-gray-800">3.4 Mass gathering cancellations:</h3>
          <span>
         It means the

cancelations of cultural, sporting, festival, and faith-
based events; conferences, meetings, trade fairs, etc.       </span><br><br>

          <h3 class="h3 mb-4 text-gray-800">3.5 Mandatory quarantine of a building or residential
area(s):</h3>
          <span>
         Refers to the quarantine and closing off of
a building or whole residential area (city, region...)      </span><br><br>

          <h3 class="h3 mb-4 text-gray-800">3.6 Environmental disinfection and use of personal
protective equipment:</h3>
          <span>
        Environmental disinfection
can and use of personal protective equipment can

limit the spread of the virus from the environment-
to-human, and human-to human, respectively.      </span>
          <hr>
          <h2 class="h3 mb-4 text-gray-800">How to implementing prevention and control
measures</h2>
          <span>
            Measures which are acceptable and feasible in one
country/setting may not be in other countries. It is
important to consider, anticipate and plan for
mitigation, while keeping in mind the considerable
public reaction that public health measures such as
social distancing. There is no one-size-fits-all
approach for implementation of prevention and
control measures of COVID-19.
While implementing social distancing, it is
important to assess its socio-economic consequences at country and individual level.
Frustration may arise in those unable to reduce
social contact in their work. Guidance will be
needed to mitigate this. Therefore countries
implementing a single intervention or combination
of interventions need to consider the following:
          </span>

          <ul>
            <li>
 Rights of individuals and groups
            </li>
            <li>
Proportionality of the response
            </li>
            <li>
Risk communication
            </li>
            <li>
Countering stigma
            </li>
            <li>
Support for people and communities, especially
for vulnerable groups
            </li>
            <li>
Promoting solidarity and mutual community
support
            </li>
            <li>
Financial compensation for lost income and
employment
            </li>
            <li>
Ensuring business continuity
            </li>
            <li>
Process and impact evaluation
            </li>

          </ul>
          <hr>
          <h2 class="h3 mb-4 text-gray-800">Recommendations</h2>
          <ul>
            <li>
Use a combination of interventions and consider
the feasibility of the intervention based on the
country’s context, and monitor progress.
            </li>
            <li>
Consider closed educational institutions to be
potential sites for managing the pandemic.
            </li>
            <li>
Implement measures to monitor shut down
places of social gathering, including restaurants,
bars, movie theaters, concerts, sporting events,
clubs, game centers, etc.
            </li>
            <li>
Seal or restrict all Ethiopian borders to all forms
of traffic/transport, and do not unseal borders
until the pandemic is under control.
            </li>
            <li>
Continue quarantining returning citizens
returning from other countries in hotels or other
facilities, and ensure these facilities are supplied
with food, water, sanitary materials,
communication capabilities, and other basic
needs.
            </li>
            <li>
Aware the people not to leave their home for
anything other than medically necessary
movement or work in support of the COVID-19
measures.
            </li>
            <li>
Begin to gradually lift the measures if the
observed reproduction number drops
sufficiently. We must be extremely vigilant and thorough with our testing in order not to stop
social distancing actions and promotion of
personal hygiene too early.
            </li>
            <li>
Aggressively quarantine families of new
patients for two to three weeks while provide
supports for those families during the quarantine
period.
            </li>
            <li>
In the preparation, take into account the
reappearance of the disease and watch case
counts for signals of reappearance.
            </li>
  <li>
Ensure a widespread surveillance and testing
capability to detect and monitor infections.
            </li>
          </ul>


